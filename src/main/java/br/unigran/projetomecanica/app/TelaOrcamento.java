    private void AddIntemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddIntemActionPerformed
        // TODO add your handling code here:
        Servico servico = (Servico) Repository.findObject(Servico.class,
                " and o.id="+Integer.valueOf(jTextField1.getText()));
        if(servico!=null){
            ItemOrcamento it = new ItemOrcamento();
            it.setServico(servico);
            it.setOrcamento(orcamento);
            it.setValor(servico.getPreco());
            orcamento.getItemOrcamentos().add(it);
            jtProduto.setModel(
                   Util.atualizaTabela(orcamento.getItemOrcamentos(), (PadraoListar) orcamento.getItemOrcamentos())
            );
            atualizaSoma();
        }else
            JOptionPane.showMessageDialog(rootPane, "Servico n√£o encontrado");
        
        
    }//GEN-LAST:event_AddIntemActionPerformed
    private void atualizaSoma(){
        Double soma=0d;
        for (ItemOrcamento itemOrcamento : orcamento.getItemOrcamentos()) {
            soma+=itemOrcamento.getValor();
        }
        jLValor.setText(soma.toString());
    }
